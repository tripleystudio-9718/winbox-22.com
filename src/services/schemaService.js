// Main function to get schema for a route
export function getSchemaForRoute(routeName) {
  // Map route names to schema keys
  const routeMapping = {
    'Home': 'home',
    'Home_zh': 'home',
    'Register': 'register',
    'Register_zh': 'register',
    'Login': 'login',
    'Login_zh': 'login',
    'Download': 'download',
    'Download_zh': 'download',
    'About': 'about',
    'About_zh': 'about',
    'Slot': 'slot',
    'Slot_zh': 'slot',
    'SportsBetting': 'sportsBetting',
    'SportsBetting_zh': 'sportsBetting',
    'LiveCasino': 'liveCasino',
    'LiveCasino_zh': 'liveCasino',
    'EkorLottery': 'ekorLottery',
    'EkorLottery_zh': 'ekorLottery',
    'HorseRacingRcb988': 'horseRacingRcb988',
    'HorseRacingRcb988_zh': 'horseRacingRcb988'
  }

  const schemaKey = routeMapping[routeName]
  
  if (schemaKey && schemaTemplates[schemaKey]) {
    return schemaTemplates[schemaKey]
  }

  // Default: no schema for unmapped pages
  return null
}

// Function to inject schema into page
export function setSchemaOrg(to) {
  // Remove existing schema if it exists
  const existingSchema = document.querySelector('script[type="application/ld+json"]')
  if (existingSchema) {
    existingSchema.remove()
  }
  
  const schemaData = getSchemaForRoute(to.name)
  
  // Only add schema if we have data for this route
  if (schemaData) {
    const schemaScript = document.createElement('script')
    schemaScript.type = 'application/ld+json'
    schemaScript.textContent = JSON.stringify(schemaData)
    document.head.appendChild(schemaScript)
  }
}